package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		
		//Print numbers from 1 to 5
		//Starting number =1
		//Ending number =5
		//Number increased by 1 in each iteration
		
	     //  1  2  3   4  5
		//Lowest to highest
		for(int i=1;i<=5;i++) {
			System.out.println(i);
		}
		
		System.out.println("-----------------");
		
		//Reverse for loop
		//Highest value to lowest value
		//Staring value =5  , ending value =1
		
		for(int i=5;i>=1;i--) {
			System.out.println(i);
		}
		
   }

}
