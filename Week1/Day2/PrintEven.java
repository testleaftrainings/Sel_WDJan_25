package week1.day2;

public class PrintEven {

	public static void main(String[] args) {
		//Print even numbers from 1 to 10
		//Even number - Divisible  by 2, Remainder should be zero
		
		for(int i=1;i<=10;i++) {
			if(i%2==0) {
				System.out.println(i);
			}
			
		}
		

	}

}
